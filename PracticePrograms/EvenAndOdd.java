public class EvenAndOdd {

	public static void main(String[] args)
	{
		//odd and even numbers

		for(int i=1;i<20;i++)
		    {
		   if(i%2==0)
		      {
		      System.out.println("the number is even :"+i);
			  
		     }else
			 System.out.println("the number is odd :"+i);

		    }
	}
	
	
}
